          <h1>About Troop 582</h1>
          <p>Troop 582 meets in Kenmore, Washington and is Sponsored by the <a href="https://nsscouts.org/"> Concerned Northshore Parents for Scouting.</a></p>
          <p>Troop meetings are presently held on the first three Mondays of the month from 7:00pm to 8:30pm unless otherwise noted on the calendar. With the Patrol Leaders Council and Scoutmasters meeting on the fourth Monday. We often plan a separate fun event for the months with a fifth Monday.</p>
          <p>Troop 582 has a very active outdoor program. We have a history of getting outdoors at least once each month, and of packing our summers full of camp and high adventure.</p>
          <p>The troop meets at <a href="https://northlakelutheran.org/">Northlake Lutheran Church</a> which has provided us with a meeting place since the unit was founded as part of their generous community outreach.</p>
